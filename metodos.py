# Método para añadir contacto

contacto = {}
def agregar():
    global contacto
    contacto = {
        "id":contacto.get('id'),
        "nombre":contacto.get('nombre'),
        "apellido":contacto.get('apellido'),
        "telefono":contacto.get('telefono')
        }
    contacto["id"] += '1'

agregar()    



